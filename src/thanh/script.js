function updateHTML(selectedIndex) {
    var introduce = document.querySelector(".introduce");
    var KTintroducesubjects = document.querySelector("#KT-introduce-subjects");

    var listCategory = document.querySelector(".listCategory");
    var tbody = listCategory.querySelector("tbody");

    var heading = document.createElement("h1");
    var para = document.createElement("p");
    var slides = document.querySelectorAll(".slide");

    heading.textContent = myData[selectedIndex].subjectTitle;
    para.textContent = myData[selectedIndex].subjectCount;

    introduce.insertBefore(para, introduce.children[0]);
    introduce.insertBefore(heading, introduce.children[0]);

    KTintroducesubjects.innerHTML = "";

    for (var i = 0; i < myData[selectedIndex].subject.length; i++) {
        var div = document.createElement("div");
        div.classList.add("KT-introduce-subjects-para");
        var p = document.createElement("p");
        p.textContent = myData[selectedIndex].subject[i];

        div.appendChild(p);
        KTintroducesubjects.appendChild(div);
    }

    tbody.innerHTML = ""; // Clear previous table data

    for (var i = 0; i < myData[selectedIndex].dataTable.length; i++) {
        var dataObj = myData[selectedIndex].dataTable[i];
        var tr = document.createElement("tr");

        for (var key in dataObj) {
            if (dataObj.hasOwnProperty(key)) {
                var td = document.createElement("td");
                td.classList.add("tg-0lax");
                td.textContent = dataObj[key];
                tr.appendChild(td);
            }
        }

        tbody.appendChild(tr);
    }

    for (var i = 0; i < slides.length; i++) {
        var slide = slides[i];
        var dataObj = myData[selectedIndex].activities[i];
        var img = document.createElement("img");
        img.setAttribute("src", myData[selectedIndex].activities[i].img);
        var p = document.createElement("p");
        p.textContent = myData[selectedIndex].activities[i].para;

        slide.innerHTML = ""; // Clear previous slide content

        slide.appendChild(img);
        slide.appendChild(p);
    }
}

var selectedIndex = 0;
updateHTML(0);

menuItem.forEach((item, index) => {
    item.addEventListener("click", () => {
        selectedIndex = index;
        menuItem.forEach((item, index) => {
            item.classList.remove("selected");
        });
        item.classList.add("selected");
        menu.classList.remove("active");

        var introduce = document.querySelector(".introduce");
        var heading = introduce.querySelector("h1");
        var para = introduce.querySelector("p");

        if (heading && para) {
            introduce.removeChild(heading);
            introduce.removeChild(para);
        }

        updateHTML(selectedIndex);
    });
});

const radioButtons = document.querySelectorAll(".radioBtn");
const frist = document.querySelector(".first");
const autoBtns = document.querySelectorAll(".auto-btn");
const prevBtn = document.querySelector(".prev");
const nextBtn = document.querySelector(".next");

let currentIndex = 0;

function updateSlider() {
    const selectedRadio = document.querySelector('input[name="radio-btn"]:checked');
    const selectedId = selectedRadio ? selectedRadio.id : radioButtons[0].id;
    const index = Number(selectedId.replace("radio", "")) - 1;
    const marginLeft = -index * 100 + "%";

    frist.style.marginLeft = marginLeft;
    frist.style.transition = "0.75s";

    autoBtns.forEach((btn, i) => {
        if (i === index) {
            btn.classList.add("active");
        } else {
            btn.classList.remove("active");
        }
    });
}

radioButtons.forEach(radioButton => {
    radioButton.addEventListener("change", updateSlider);
});

function plusSlides(n) {
    currentIndex += n;
    if (currentIndex < 0) {
        currentIndex = radioButtons.length - 1;
    } else if (currentIndex >= radioButtons.length) {
        currentIndex = 0;
    }
    radioButtons[currentIndex].checked = true;
    updateSlider();
}

setInterval(() => {
    plusSlides(1);
}, 3000);

const manualBtns = document.querySelectorAll(".manual-btn");

manualBtns.forEach((btn, index) => {
    btn.addEventListener("click", () => {
        currentIndex = index;
        radioButtons[currentIndex].checked = true;
        updateSlider();
    });
});

prevBtn.addEventListener("click", () => {
    plusSlides(-1);
});

nextBtn.addEventListener("click", () => {
    plusSlides(1);
});
